#include<stdio.h>
#include<math.h>

float function(float x)
{
	return 4*x*x*x + 8*x;
}

int main()
{
	float x[100],y[100],a,b,h,evens=0,odds=0,simpson;
	int n,i;
	
	printf("\n Enter the Interval (a,b)  ");
	printf("\n a : ");
	scanf("%f",&a);
        printf("\n b : ");
        scanf("%f",&b);

	printf("\n Enter value of h : ");
	scanf("%f",&h);

	n=b-a;
	n=n/h;

	if(n%2)
		n=n+1;
	
	h=b-a;
	h=h/n;
	x[0]=a;
	printf(" i \t x \t\t f(x)\n");
	for(i=1;i<n;i++){
		x[i]=x[i-1]+h;
		if(i%2)
			odds= odds + function(x[i]);
		else
			evens=evens + function(x[i]);

	printf(" %d \t %f \t %f \t\n",i,x[i],function(x[i])); 
	}

	simpson = ((h/3)*( function(a) + function(b) + 4*odds + 2*evens) );	
	printf("\n\n The Value of the integral is %f \n\n",simpson);
	
}
